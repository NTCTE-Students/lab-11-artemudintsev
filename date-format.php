<?php
$dates = [
    "31.12.2024",
    "01.01.1990",
    "32.01.2024", // ошибка
    "15.13.2022", // ошибка
];

$pattern = "/^(0[1-9]|[12][0-9]|3[01])\.(0[1-9]|1[0-2])\.(19|20)\d\d$/";

foreach ($dates as $date) {
    echo "$date: " . (preg_match($pattern, $date) ? "корректна" : "ошибка") . "<br>";
}
